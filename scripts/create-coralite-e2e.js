import { execSync } from 'node:child_process'
import { mkdtemp, rm } from 'node:fs/promises'
import { tmpdir } from 'node:os'
import { join } from 'node:path'
import { existsSync } from 'node:fs'
import fs from 'node:fs'

const tempDir = await mkdtemp(join(tmpdir(), 'coralite-e2e-'))
const projectName = 'test-app'
const projectPath = join(tempDir, projectName)

console.log(`🚀 Starting E2E smoke test in ${tempDir}...`)

try {
  const cliPath = join(process.cwd(), 'packages/create-coralite/bin/index.js')
  if (!existsSync(cliPath)) {
    throw new Error(`CLI not found at ${cliPath}`)
  }

  console.log(`\n📦 Scaffolding project...`)
  // Pass -o to bypass prompts for project name and output dir, --template for template
  execSync(`node "${cliPath}" -o "${projectName}" --template css`, {
    cwd: tempDir,
    stdio: 'inherit'
  })

  if (!existsSync(projectPath)) {
    throw new Error(`Scaffolded project directory ${projectPath} does not exist`)
  }


  console.log('\n📦 Packing local workspace packages...')
  execSync('pnpm run build', {
    cwd: process.cwd(),
    stdio: 'inherit'
  })

  execSync('pnpm pack', {
    cwd: join(process.cwd(), 'packages/coralite'),
    stdio: 'inherit'
  })
  execSync('mv packages/coralite/*.tgz .', {
    cwd: process.cwd(),
    stdio: 'inherit'
  })
  execSync('pnpm pack', {
    cwd: join(process.cwd(), 'packages/coralite-scripts'),
    stdio: 'inherit'
  })
  execSync('mv packages/coralite-scripts/*.tgz .', {
    cwd: process.cwd(),
    stdio: 'inherit'
  })

  const pkgJsonPath = join(projectPath, 'package.json')
  const pkgJson = JSON.parse(await import('node:fs/promises').then(fs => fs.readFile(pkgJsonPath, 'utf8')))

  // Find the exact filenames generated by npm pack
  const coraliteTar = fs.readdirSync(process.cwd()).find(f => f.startsWith('coralite-') && !f.startsWith('coralite-scripts') && f.endsWith('.tgz'))
  const scriptsTar = fs.readdirSync(process.cwd()).find(f => f.startsWith('coralite-scripts-') && f.endsWith('.tgz'))

  pkgJson.devDependencies['coralite'] = 'file:' + join(process.cwd(), coraliteTar)
  pkgJson.devDependencies['coralite-scripts'] = 'file:' + join(process.cwd(), scriptsTar)

  await import('node:fs/promises').then(fs => fs.writeFile(pkgJsonPath, JSON.stringify(pkgJson, null, 2)))


  // Install dependencies
  console.log(`\n⬇️ Installing dependencies...`)
  // The scaffolded project might use npm or yarn, but npm install should work generally
  // Alternatively, we use `npm install`
  execSync('npm install', {
    cwd: projectPath,
    stdio: 'inherit'
  })

  // Run the build command
  console.log(`\n🛠️ Running build script...`)
  execSync('npm run build', {
    cwd: projectPath,
    stdio: 'inherit'
  })

  console.log(`\n✅ Smoke test passed successfully!`)

} catch (error) {
  console.error(`\n❌ Smoke test failed!`, error.message)
  process.exit(1)
} finally {
  console.log(`\n🧹 Cleaning up ${tempDir}...`)
  await rm(tempDir, {
    recursive: true,
    force: true
  })
}
